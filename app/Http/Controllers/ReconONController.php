     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $con = Consignment::where('consignment_unique_id', $request->consignment_unique_id)->where('status', 2)->first();
        if(count($con) == 0){
            return 'FAILED!! <a href="recon-on">Back</a>';
        }else{
            $con->status = 3;
            if($con->save()){
                return 'SUCCESS!! <a href="recon-on">Back</a>';
            }else{
                return 'FAILED!! <a href="recon-on">Back</a>';
            }
        }
    }

